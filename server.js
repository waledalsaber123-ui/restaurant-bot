/* ================= HELPERS ================= */
async function sendWA(chatId, message) {
  try {
    await axios.post(`${SETTINGS.API_URL}/sendMessage/${SETTINGS.GREEN_TOKEN}`, { chatId, message });
  } catch (err) { console.log("WA ERROR:", err.message); }
}

/* ================= WEBHOOK ================= */
app.post("/webhook", async (req, res) => {
  res.sendStatus(200);
  const body = req.body;
  if (body.typeWebhook !== "incomingMessageReceived") return;

  const chatId = body.senderData?.chatId;
  const text = body.messageData?.textMessageData?.textMessage || 
               body.messageData?.extendedTextMessageData?.text || "";

  if (!chatId || chatId.endsWith("@g.us") || !text.trim()) return;

  // تهيئة الجلسة
  if (!SESSIONS[chatId]) {
      SESSIONS[chatId] = { history: [], lastActive: Date.now() };
  }

  // === ميزة "طلب جديد" ===
  if (text.includes("طلب جديد")) {
      SESSIONS[chatId] = { history: [], lastActive: Date.now() };
      await sendWA(chatId, "أبشر يا غالي، مسحنا الطلب القديم. تفضل شو حابب تطلب هسه؟ 🍔");
      return;
  }

  const session = SESSIONS[chatId];
  session.lastActive = Date.now();

  try {
    const ai = await axios.post("https://api.openai.com/v1/chat/completions", {
      model: "gpt-4o-mini",
      messages: [
        { role: "system", content: SYSTEM_PROMPT },
        // زيادة الذاكرة لتشمل آخر 15 رسالة لضمان عدم النسيان
        ...session.history.slice(-15), 
        { role: "user", content: text }
      ],
      temperature: 0.1 // لمنع الهلوسة والتأليف
    }, {
      headers: { Authorization: `Bearer ${SETTINGS.OPENAI_KEY}` },
      timeout: 15000
    });

    let reply = ai.data.choices[0].message.content;

    // إرسال الطلب للمطبخ
    if (reply.includes("[KITCHEN_GO]")) {
      const orderToKitchen = reply.replace("[KITCHEN_GO]", "🔔 *طلب جديد من البوت!*").trim();
      await sendWA(SETTINGS.KITCHEN_GROUP, orderToKitchen);
      await sendWA(chatId, "تم إرسال طلبك للمطبخ بنجاح! 👨‍🍳✅\nرح نتواصل معك قريباً.");
      delete SESSIONS[chatId];
      return;
    }

    await sendWA(chatId, reply);
    
    // حفظ المحادثة
    session.history.push({ role: "user", content: text }, { role: "assistant", content: reply });

  } catch (err) {
    console.log("AI ERROR:", err.response?.data || err.message);
    await sendWA(chatId, "يا غالي صار عندي تعليق بسيط، ممكن تعيد آخر رسالة؟ 🙏");
  }
});

