/* ================= WEBHOOK ================= */

app.post("/webhook", async (req, res) => {
  res.sendStatus(200);
  const body = req.body;
  if (body.typeWebhook !== "incomingMessageReceived") return;

  const chatId = body.senderData?.chatId;
  const text = body.messageData?.textMessageData?.textMessage || 
               body.messageData?.extendedTextMessageData?.text || "";

  if (!chatId || chatId.includes("@g.us") || !text.trim()) return;

  if (!SESSIONS[chatId]) SESSIONS[chatId] = { history: [], lastActive: Date.now() };
  const session = SESSIONS[chatId];

  if (text.includes("طلب جديد")) {
      SESSIONS[chatId] = { history: [], lastActive: Date.now() };
      await sendWA(chatId, "أبشر، مسحنا كل شي. تفضل شو حابب تطلب هسه؟ 🍔");
      return;
  }

  try {
    const ai = await axios.post("https://api.openai.com/v1/chat/completions", {
      model: "gpt-4o-mini",
      messages: [
        { role: "system", content: SYSTEM_PROMPT + "\n" + FULL_DATA },
        ...session.history.slice(-10),
        { role: "user", content: text }
      ],
      temperature: 0.2
    }, {
      headers: { Authorization: `Bearer ${SETTINGS.OPENAI_KEY}` }
    });

    let reply = ai.data.choices[0].message.content;

    if (reply.includes("[KITCHEN_GO]")) {
      const kitchenMsg = reply.replace("[KITCHEN_GO]", "🔔 *طلب جديد مؤكد!*").trim();
      await sendWA(SETTINGS.KITCHEN_GROUP, kitchenMsg);
      await sendWA(chatId, "تم إرسال طلبك للمطبخ! صحتين وعافية مسبقاً. 🙏🔥");
      delete SESSIONS[chatId];
      return;
    }

    await sendWA(chatId, reply);
    session.history.push({ role: "user", content: text }, { role: "assistant", content: reply });

  } catch (err) { console.error("Error"); }
});

async function sendWA(chatId, message) {
  try {
    await axios.post(`${SETTINGS.API_URL}/sendMessage/${SETTINGS.GREEN_TOKEN}`, { chatId, message });
  } catch (err) { console.error("WA ERROR"); }
}

